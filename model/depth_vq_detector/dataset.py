def _resolve(base: Path, value: str | None) -> str | None:
    if value is None:
        return None
    p = Path(value)
    if p.is_absolute():
        return str(p)
    return str((base / p).resolve())


def _json_loads_maybe(value: Any) -> dict[str, Any]:
    if isinstance(value, np.ndarray):
        if value.shape == ():
            value = value.item()
        else:
            value = str(value)
    if isinstance(value, bytes):
        value = value.decode("utf-8")
    if isinstance(value, str):
        try:
            return json.loads(value)
        except json.JSONDecodeError:
            return {}
    if isinstance(value, dict):
        return value
    return {}


def canonical_cad_aliases(cad_id: str) -> set[str]:
    """Return common aliases for robust STL/CAD memory matching.

    The 2D dataset stores `stl` such as `03_sol_block_front.stl`. The 3D
    point-cloud dataset generated by stl_to_pointcloud_dataset.py may store CAD
    ids with a deterministic suffix, e.g. `03_sol_block_front__e79f118d`.
    This function normalizes both conventions so they can match.
    """
    import re

    raw = str(cad_id).strip()
    if raw == "":
        return set()

    candidates: set[str] = set()

    def add_variants(value: str) -> None:
        value = str(value).strip()
        if not value:
            return
        p = Path(value)
        vals = {value, p.name, p.stem}
        # Strip generated hash suffixes like __e79f118d from point-cloud CAD ids.
        vals |= {re.sub(r"__[0-9a-fA-F]{6,}$", "", v) for v in list(vals)}
        for v in list(vals):
            if v and Path(v).suffix == "":
                vals.add(v + ".stl")
        for v in vals:
            if v:
                candidates.add(v)
                candidates.add(v.lower())

    add_variants(raw)
    return {c for c in candidates if c}


def build_cad_alias_map(cad_ids: Iterable[str]) -> dict[str, int]:
    mapping: dict[str, int] = {}
    for idx, cad_id in enumerate(cad_ids):
        for alias in canonical_cad_aliases(str(cad_id)):
            mapping.setdefault(alias, idx)
    return mapping


def lookup_cad_index(cad_id_to_index: dict[str, int], *candidates: str | None) -> int:
    for candidate in candidates:
        if not candidate:
            continue
        for alias in canonical_cad_aliases(str(candidate)):
            if alias in cad_id_to_index:
                return int(cad_id_to_index[alias])
    return -1


def _read_scene_source(source: str | Path | list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Read either the provided root format, a JSON manifest, one scene npz, or an in-memory scene list."""
    if isinstance(source, list):
        out: list[dict[str, Any]] = []
        for item in source:
            if not isinstance(item, dict):
                raise TypeError(f"In-memory scene entries must be dicts, got {type(item)}")
            entry = dict(item)
            if "scene_npz" in entry and entry["scene_npz"] is not None:
                entry["scene_npz"] = str(Path(entry["scene_npz"]).expanduser().resolve())
            out.append(entry)
        return out

    source = Path(source).resolve()
    if source.is_dir():
        npz_dir = source / "npz"
        if npz_dir.exists():
            files = sorted(npz_dir.glob("scene_*.npz")) + sorted(npz_dir.glob("scene*.npz"))
        else:
            files = sorted(source.glob("scene_*.npz")) + sorted(source.glob("scene*.npz"))
        # Remove duplicates caused by the two glob patterns.
        seen = set()
        unique_files = []
        for f in files:
            if f not in seen:
                unique_files.append(f)
                seen.add(f)
        return [{"scene_id": f.stem, "scene_npz": str(f)} for f in unique_files]

    if source.suffix.lower() == ".npz":
        return [{"scene_id": source.stem, "scene_npz": str(source)}]

    if source.suffix.lower() != ".json":
        raise ValueError(f"Unsupported dataset source: {source}. Use dataset root, scene npz, or JSON manifest.")

    base = source.parent
    with source.open("r", encoding="utf-8") as f:
        data = json.load(f)
    scenes = data.get("scenes", data if isinstance(data, list) else None)
    if scenes is None:
        raise ValueError("Manifest must be a list or a dict with key 'scenes'.")
    out = []
    for s in scenes:
        item = dict(s)
        for key in [
            "scene_npz",
            "depth",
            "instance",
            "instance_mask",
            "instance_id",
            "semantic",
            "semantic_mask",
            "semantic_id",
            "camera",
            "annotations",
        ]:
            if key in item and item[key] is not None:
                item[key] = _resolve(base, item[key])
        out.append(item)
    return out


def _boxes_from_mask(mask: torch.Tensor) -> torch.Tensor:
    y, x = torch.where(mask > 0)
    if x.numel() == 0:
        return torch.zeros((4,), dtype=torch.float32)
    h, w = mask.shape
    x0, x1 = x.min().float(), x.max().float() + 1.0
    y0, y1 = y.min().float(), y.max().float() + 1.0
    return torch.tensor([x0 / w, y0 / h, x1 / w, y1 / h], dtype=torch.float32)


class DepthInstanceDataset(Dataset):
    """Depth-only instance dataset for the provided scene-level .npz format.

    Primary supported layout:
      root/
        npz/scene_00000.npz
        crops/scene00000_inst01_cat03.npz
        vis/...

    Each scene npz must contain:
      depth       [H,W] float32, NaN background
      inst_id     [H,W] int32, 0 background, 1..N instances
      category_id [H,W] int32, 0 background, 1..27 classes
      meta        JSON string with instances[inst_id].stl / quat_wxyz / euler_zyx_deg

    The detector is trained on full scenes because inference must detect multiple
    objects from a full depth image. Crop npz files are useful for auxiliary
    CAD-retrieval pretraining, but are not used as the main detector input.
    """

    def __init__(
        self,
        source: str | Path | list[dict[str, Any]],
        cad_id_to_index: dict[str, int] | None = None,
        input_mode: str = "zv",
        image_size: tuple[int, int] | None = None,
        depth_scale: float | None = None,
        label_offset: int = 1,
        min_mask_area: int = 4,
    ) -> None:
        self.source = Path(source)
        self.scenes = _read_scene_source(source)
        if not self.scenes:
            raise ValueError(f"No scene npz files found from source: {source}")
        self.cad_id_to_index = cad_id_to_index or {}
        self.input_mode = input_mode
        self.image_size = image_size
        self.depth_scale = depth_scale
        self.label_offset = int(label_offset)
        self.min_mask_area = int(min_mask_area)

    def __len__(self) -> int:
        return len(self.scenes)

    def _annotation_path(self, scene: dict[str, Any]) -> str | None:
        return scene.get("annotations") or scene.get("annotation")

    def _instance_path(self, scene: dict[str, Any]) -> str | None:
        return scene.get("instance_id") or scene.get("instance_mask") or scene.get("instance")

    def _semantic_path(self, scene: dict[str, Any]) -> str | None:
        return scene.get("semantic_id") or scene.get("semantic_mask") or scene.get("semantic")

    def _load_annotations(self, scene: dict[str, Any]) -> list[dict[str, Any]]:
        ann_path = self._annotation_path(scene)
        if ann_path is None:
            return []
        ann = load_json(ann_path)
        if isinstance(ann, list):
            return ann
        return ann.get("instances", [])

    def _load_from_scene_npz(self, path: str | Path) -> tuple[np.ndarray, np.ndarray, np.ndarray, dict[str, Any], dict[str, Any]]:
        data = np.load(path, allow_pickle=True)
        required = {"depth", "inst_id", "category_id"}
        missing = required.difference(data.files)
        if missing:
            raise KeyError(f"{path} missing required keys {sorted(missing)}. Available keys: {data.files}")
        depth = data["depth"].astype(np.float32)
        if self.depth_scale is not None:
            depth = depth * float(self.depth_scale)
        instance_map = data["inst_id"].astype(np.int64)
        semantic_map = data["category_id"].astype(np.int64)
        meta = _json_loads_maybe(data["meta"]) if "meta" in data.files else {}
        camera = meta.get("camera", {}) if isinstance(meta.get("camera", {}), dict) else {}
        return depth, instance_map, semantic_map, meta, camera

    def _load_legacy_scene(self, scene: dict[str, Any]) -> tuple[np.ndarray, np.ndarray | None, np.ndarray | None, dict[str, Any], dict[str, Any]]:
        depth_path = scene.get("depth")
        if depth_path is None:
            raise KeyError("Legacy manifest entry has no depth path.")
        camera = load_json(scene.get("camera"))
        scale = self.depth_scale
        if scale is None and "depth_scale" in camera:
            scale = float(camera["depth_scale"])
        depth = load_depth(depth_path, depth_scale=scale)
        inst_path = self._instance_path(scene)
        sem_path = self._semantic_path(scene)
        instance_map = load_id_map(inst_path) if inst_path else None
        semantic_map = load_id_map(sem_path) if sem_path else None
        anns = self._load_annotations(scene)
        meta = {"instances": {str(a.get("instance_id", a.get("id", -1))): a for a in anns}}
        return depth, instance_map, semantic_map, meta, camera

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, dict[str, torch.Tensor | str | list[str]]]:
        scene = self.scenes[idx]
        if "scene_npz" in scene:
            depth, instance_map, semantic_map, meta, camera = self._load_from_scene_npz(scene["scene_npz"])
            scene_id = str(meta.get("scene_idx", Path(scene["scene_npz"]).stem))
            if scene_id.isdigit():
                scene_id = f"scene_{int(scene_id):05d}"
        else:
            depth, instance_map, semantic_map, meta, camera = self._load_legacy_scene(scene)
            scene_id = str(scene.get("scene_id", Path(scene.get("depth", "scene")).stem))

        depth, instance_map, semantic_map = resize_depth_and_masks(depth, instance_map, semantic_map, self.image_size)
        if instance_map is None:
            raise ValueError(f"Scene {scene_id} has no instance map. The detector requires inst_id for training.")
        x = make_depth_input(depth, camera=camera, mode=self.input_mode)
        image = torch.from_numpy(x).float()
        h, w = depth.shape

        instances_meta_raw = meta.get("instances", {}) if isinstance(meta, dict) else {}
        instances_meta: dict[int, dict[str, Any]] = {}
        if isinstance(instances_meta_raw, dict):
            for k, v in instances_meta_raw.items():
                try:
                    instances_meta[int(k)] = dict(v)
                except Exception:
                    continue
        visible_ids = meta.get("visible_inst_ids") if isinstance(meta, dict) else None
        if visible_ids is not None:
            instance_values = [int(v) for v in visible_ids if int(v) != 0]
        else:
            instance_values = sorted([int(v) for v in np.unique(instance_map) if int(v) != 0])
        available = set(int(v) for v in np.unique(instance_map) if int(v) != 0)
        instance_values = [v for v in instance_values if v in available]

        masks = []
        boxes_xyxy = []
        labels = []
        cad_indices = []
        cad_id_strings = []
        stl_strings = []
        instance_ids = []
        quat_list = []
        euler_list = []
        raw_category_ids = []

        for instance_id in instance_values:
            mask_np = instance_map == instance_id
            if int(mask_np.sum()) < self.min_mask_area:
                continue
            mask_t = torch.from_numpy(mask_np.astype(np.uint8)).bool()
            inst_meta = instances_meta.get(instance_id, {})

            category_id = inst_meta.get("category_id")
            if category_id is None and semantic_map is not None:
                vals = semantic_map[mask_np]
                vals = vals[vals != 0]
                category_id = int(np.bincount(vals.astype(np.int64)).argmax()) if vals.size else 0
            category_id = int(category_id or 0)
            label = category_id - self.label_offset
            if label < 0:
                continue

            stl = str(inst_meta.get("stl", ""))
            cad_id = str(inst_meta.get("cad_id", stl)) if inst_meta.get("cad_id", stl) is not None else ""
            cad_index = lookup_cad_index(self.cad_id_to_index, cad_id, stl, Path(stl).stem if stl else None)

            quat = np.asarray(inst_meta.get("quat_wxyz", [1.0, 0.0, 0.0, 0.0]), dtype=np.float32)
            if quat.shape != (4,):
                quat = np.array([1.0, 0.0, 0.0, 0.0], dtype=np.float32)
            euler = np.asarray(inst_meta.get("euler_zyx_deg", [0.0, 0.0, 0.0]), dtype=np.float32)
            if euler.shape != (3,):
                euler = np.zeros((3,), dtype=np.float32)

            masks.append(mask_t)
            boxes_xyxy.append(_boxes_from_mask(mask_t))
            labels.append(label)
            cad_indices.append(cad_index)
            cad_id_strings.append(cad_id)
            stl_strings.append(stl)
            instance_ids.append(instance_id)
            quat_list.append(torch.from_numpy(quat))
            euler_list.append(torch.from_numpy(euler))
            raw_category_ids.append(category_id)

        if masks:
            masks_t = torch.stack(masks).float()
            boxes_xyxy_t = torch.stack(boxes_xyxy).float()
            boxes_t = box_xyxy_to_cxcywh(boxes_xyxy_t)
            labels_t = torch.tensor(labels, dtype=torch.long)
            cad_ids_t = torch.tensor(cad_indices, dtype=torch.long)
            instance_ids_t = torch.tensor(instance_ids, dtype=torch.long)
            quats_t = torch.stack(quat_list).float()
            eulers_t = torch.stack(euler_list).float()
            raw_category_ids_t = torch.tensor(raw_category_ids, dtype=torch.long)
        else:
            masks_t = torch.zeros((0, h, w), dtype=torch.float32)
            boxes_t = torch.zeros((0, 4), dtype=torch.float32)
            boxes_xyxy_t = torch.zeros((0, 4), dtype=torch.float32)
            labels_t = torch.zeros((0,), dtype=torch.long)
            cad_ids_t = torch.full((0,), -1, dtype=torch.long)
            instance_ids_t = torch.zeros((0,), dtype=torch.long)
            quats_t = torch.zeros((0, 4), dtype=torch.float32)
            eulers_t = torch.zeros((0, 3), dtype=torch.float32)
            raw_category_ids_t = torch.zeros((0,), dtype=torch.long)

        target: dict[str, torch.Tensor | str | list[str]] = {
            "masks": masks_t,
            "boxes": boxes_t,              # normalized cxcywh
            "boxes_xyxy": boxes_xyxy_t,    # normalized xyxy
            "labels": labels_t,            # 0-based class labels for CE
            "category_ids_raw": raw_category_ids_t,  # original 1..27 ids
            "cad_ids": cad_ids_t,
            "instance_ids": instance_ids_t,
            "quat_wxyz": quats_t,
            "euler_zyx_deg": eulers_t,
            "scene_id": scene_id,
            "cad_id_strings": cad_id_strings,
            "stl_strings": stl_strings,
            "orig_size": torch.tensor([h, w], dtype=torch.long),
        }
        return image, target


def collate_fn(batch: list[tuple[torch.Tensor, dict[str, Any]]]) -> tuple[torch.Tensor, list[dict[str, Any]]]:
    images, targets = zip(*batch)
    shapes = {tuple(img.shape) for img in images}
    if len(shapes) != 1:
        raise ValueError(f"All images in a batch must have the same shape. Got {shapes}. Use --image_size.")
    return torch.stack(list(images), dim=0), list(targets)
