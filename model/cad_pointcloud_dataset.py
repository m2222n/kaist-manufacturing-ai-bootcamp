"""
Minimal PyTorch dataset for point clouds generated by stl_to_pointcloud_dataset.py.

Usage:
    from cad_pointcloud_dataset import CADPointCloudDataset
    ds = CADPointCloudDataset('./pc_dataset/manifest.json', augment=True)
    item = ds[0]
    # item['features']: FloatTensor (N, 6), [canonical xyz, normal]
"""

from __future__ import annotations

import json
import math
from pathlib import Path
from typing import Dict, Optional

import numpy as np
import torch
from torch.utils.data import Dataset


class CADPointCloudDataset(Dataset):
    def __init__(
        self,
        manifest_path: str | Path,
        n_points: Optional[int] = None,
        augment: bool = False,
        use_normals: bool = True,
        return_raw: bool = False,
    ):
        self.manifest_path = Path(manifest_path)
        with open(self.manifest_path, "r", encoding="utf-8") as f:
            self.manifest = json.load(f)
        self.items = self.manifest["items"]
        self.n_points = n_points
        self.augment = augment
        self.use_normals = use_normals
        self.return_raw = return_raw

        class_names = sorted({item["class_name"] for item in self.items})
        self.class_to_idx = {name: i for i, name in enumerate(class_names)}

    def __len__(self) -> int:
        return len(self.items)

    @staticmethod
    def _random_z_rotation(points: np.ndarray, normals: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
        theta = np.random.uniform(0.0, 2.0 * math.pi)
        c, s = math.cos(theta), math.sin(theta)
        R = np.asarray([[c, -s, 0.0], [s, c, 0.0], [0.0, 0.0, 1.0]], dtype=np.float32)
        return points @ R.T, normals @ R.T

    @staticmethod
    def _jitter(points: np.ndarray, sigma: float = 0.002, clip: float = 0.01) -> np.ndarray:
        noise = np.clip(np.random.normal(0.0, sigma, size=points.shape), -clip, clip).astype(np.float32)
        return points + noise

    @staticmethod
    def _random_point_dropout(points: np.ndarray, normals: np.ndarray, max_drop_ratio: float = 0.2) -> tuple[np.ndarray, np.ndarray]:
        n = len(points)
        keep_ratio = 1.0 - np.random.uniform(0.0, max_drop_ratio)
        keep_n = max(16, int(round(n * keep_ratio)))
        idx = np.random.choice(n, size=keep_n, replace=False)
        # Resample back to n points to keep tensor shape fixed.
        if keep_n < n:
            refill = np.random.choice(idx, size=n - keep_n, replace=True)
            idx = np.concatenate([idx, refill])
        np.random.shuffle(idx)
        return points[idx], normals[idx]

    def __getitem__(self, index: int) -> Dict:
        item = self.items[index]
        data = np.load(item["npz_path"])

        points = data["points"].astype(np.float32)      # canonical xyz
        normals = data["normals"].astype(np.float32)

        if self.n_points is not None and self.n_points < len(points):
            idx = np.random.choice(len(points), size=self.n_points, replace=False)
            points = points[idx]
            normals = normals[idx]
        elif self.n_points is not None and self.n_points > len(points):
            idx = np.random.choice(len(points), size=self.n_points, replace=True)
            points = points[idx]
            normals = normals[idx]

        if self.augment:
            # For CAD retrieval pretraining, rotation augmentation should respect your canonical axis policy.
            # Use only when class identity is rotation-invariant around z or when pose should be factored out.
            points, normals = self._random_point_dropout(points, normals)
            points = self._jitter(points)

        if self.use_normals:
            features = np.concatenate([points, normals], axis=1).astype(np.float32)
        else:
            features = points.astype(np.float32)

        out = {
            "cad_id": item["cad_id"],
            "class_name": item["class_name"],
            "class_id": torch.tensor(self.class_to_idx[item["class_name"]], dtype=torch.long),
            "points": torch.from_numpy(points),
            "normals": torch.from_numpy(normals),
            "features": torch.from_numpy(features),
        }
        if self.return_raw:
            out["points_raw"] = torch.from_numpy(data["points_raw"].astype(np.float32))
            out["center"] = torch.from_numpy(data["center"].astype(np.float32))
            out["scale"] = torch.tensor(float(data["scale"]), dtype=torch.float32)
        return out
