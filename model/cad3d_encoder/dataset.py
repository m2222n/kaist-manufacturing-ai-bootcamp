"""
Dataset utilities for point clouds generated by stl_to_pointcloud_dataset.py.

Supports two augmented views per CAD item for contrastive CAD-identity training.
"""

from __future__ import annotations

import json
import math
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset


class PointCloudAugmentor:
    def __init__(
        self,
        jitter_sigma: float = 0.002,
        jitter_clip: float = 0.01,
        dropout_max: float = 0.2,
        scale_range: Tuple[float, float] = (0.98, 1.02),
        rotation_aug: str = "none",
    ):
        if rotation_aug not in {"none", "z", "so3"}:
            raise ValueError("rotation_aug must be one of: none, z, so3")
        self.jitter_sigma = jitter_sigma
        self.jitter_clip = jitter_clip
        self.dropout_max = dropout_max
        self.scale_range = scale_range
        self.rotation_aug = rotation_aug

    @staticmethod
    def _rotation_z() -> np.ndarray:
        theta = np.random.uniform(0.0, 2.0 * math.pi)
        c, s = math.cos(theta), math.sin(theta)
        return np.asarray([[c, -s, 0.0], [s, c, 0.0], [0.0, 0.0, 1.0]], dtype=np.float32)

    @staticmethod
    def _rotation_so3() -> np.ndarray:
        # Uniform random unit quaternion -> rotation matrix.
        u1, u2, u3 = np.random.rand(3)
        q1 = math.sqrt(1 - u1) * math.sin(2 * math.pi * u2)
        q2 = math.sqrt(1 - u1) * math.cos(2 * math.pi * u2)
        q3 = math.sqrt(u1) * math.sin(2 * math.pi * u3)
        q4 = math.sqrt(u1) * math.cos(2 * math.pi * u3)
        x, y, z, w = q1, q2, q3, q4
        return np.asarray(
            [
                [1 - 2 * (y * y + z * z), 2 * (x * y - z * w), 2 * (x * z + y * w)],
                [2 * (x * y + z * w), 1 - 2 * (x * x + z * z), 2 * (y * z - x * w)],
                [2 * (x * z - y * w), 2 * (y * z + x * w), 1 - 2 * (x * x + y * y)],
            ],
            dtype=np.float32,
        )

    def __call__(self, points: np.ndarray, normals: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
        n = len(points)

        # Point dropout followed by resampling keeps fixed tensor shape.
        if self.dropout_max > 0:
            keep_ratio = 1.0 - np.random.uniform(0.0, self.dropout_max)
            keep_n = max(16, int(round(n * keep_ratio)))
            idx = np.random.choice(n, size=keep_n, replace=False)
            if keep_n < n:
                refill = np.random.choice(idx, size=n - keep_n, replace=True)
                idx = np.concatenate([idx, refill])
            np.random.shuffle(idx)
            points = points[idx]
            normals = normals[idx]

        if self.scale_range is not None:
            scale = np.random.uniform(self.scale_range[0], self.scale_range[1])
            points = points * np.float32(scale)

        if self.rotation_aug == "z":
            R = self._rotation_z()
            points = points @ R.T
            normals = normals @ R.T
        elif self.rotation_aug == "so3":
            R = self._rotation_so3()
            points = points @ R.T
            normals = normals @ R.T

        if self.jitter_sigma > 0:
            noise = np.clip(
                np.random.normal(0.0, self.jitter_sigma, size=points.shape),
                -self.jitter_clip,
                self.jitter_clip,
            ).astype(np.float32)
            points = points + noise

        return points.astype(np.float32), normals.astype(np.float32)


class CADPointCloudContrastiveDataset(Dataset):
    def __init__(
        self,
        manifest_path: str | Path,
        n_points: Optional[int] = None,
        use_normals: bool = True,
        two_views: bool = True,
        augment: bool = True,
        rotation_aug: str = "none",
    ):
        self.manifest_path = Path(manifest_path)
        with open(self.manifest_path, "r", encoding="utf-8") as f:
            self.manifest = json.load(f)
        self.items: List[Dict] = self.manifest["items"]
        self.n_points = n_points
        self.use_normals = use_normals
        self.two_views = two_views
        self.augment = augment
        self.augmentor = PointCloudAugmentor(rotation_aug=rotation_aug)

        class_names = sorted({item.get("class_name", "unknown") for item in self.items})
        cad_ids = sorted({item["cad_id"] for item in self.items})
        self.class_to_idx = {name: i for i, name in enumerate(class_names)}
        self.cad_to_idx = {cad_id: i for i, cad_id in enumerate(cad_ids)}
        self.idx_to_class = {i: name for name, i in self.class_to_idx.items()}
        self.idx_to_cad = {i: cad_id for cad_id, i in self.cad_to_idx.items()}

    def __len__(self) -> int:
        return len(self.items)

    def _resolve_npz_path(self, item: Dict) -> Path:
        path = Path(item["npz_path"])
        if path.exists():
            return path
        candidate = self.manifest_path.parent / path.name
        if candidate.exists():
            return candidate
        raise FileNotFoundError(f"Cannot find point cloud npz: {path} or {candidate}")

    def _load_arrays(self, item: Dict) -> Tuple[np.ndarray, np.ndarray]:
        data = np.load(self._resolve_npz_path(item))
        points = data["points"].astype(np.float32)
        normals = data["normals"].astype(np.float32)

        if self.n_points is not None:
            if self.n_points < len(points):
                idx = np.random.choice(len(points), size=self.n_points, replace=False)
            elif self.n_points > len(points):
                idx = np.random.choice(len(points), size=self.n_points, replace=True)
            else:
                idx = np.arange(len(points))
            points = points[idx]
            normals = normals[idx]

        return points, normals

    def _make_features(self, points: np.ndarray, normals: np.ndarray) -> torch.Tensor:
        if self.use_normals:
            features = np.concatenate([points, normals], axis=1).astype(np.float32)
        else:
            features = points.astype(np.float32)
        return torch.from_numpy(features)

    def __getitem__(self, index: int) -> Dict:
        item = self.items[index]
        points, normals = self._load_arrays(item)

        if self.augment:
            points1, normals1 = self.augmentor(points.copy(), normals.copy())
        else:
            points1, normals1 = points.copy(), normals.copy()

        out = {
            "features": self._make_features(points1, normals1),
            "cad_id": item["cad_id"],
            "class_name": item.get("class_name", "unknown"),
            "cad_idx": torch.tensor(self.cad_to_idx[item["cad_id"]], dtype=torch.long),
            "class_idx": torch.tensor(self.class_to_idx[item.get("class_name", "unknown")], dtype=torch.long),
        }

        if self.two_views:
            if self.augment:
                points2, normals2 = self.augmentor(points.copy(), normals.copy())
            else:
                points2, normals2 = points.copy(), normals.copy()
            out["features_view2"] = self._make_features(points2, normals2)

        return out
